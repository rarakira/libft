int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	i;
	int	signed_n;

	signed_n = (int)n;
	i = 0;
	while ((s1[i] != '\0' || s2[i] != '\0') && i < signed_n)
	{
		if (s1[i] == s2[i])
			i++;
		else
			return (s1[i] - s2[i]);
	}
	return (0);
}
